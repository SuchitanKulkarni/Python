# Operators in Python:
# Arithmetic Operators:
# (+,-,/,*)
a=10
b=20
print(a,b)
print(a+b)
print(a-b)
print(a*b)
print(a/b)
# Relational Operators:
# (<,>,==,!=)
a=50
b=100
print(a>b)
print(a<b)
print(a==b)
print(a!=b)
# Logical Operators:
# (&,|)
a=True
b=False
print(a&a)
print(a&b)
print(b&a)
print(b&b)
print(a|b)
print(a|a)
print(b|b)


